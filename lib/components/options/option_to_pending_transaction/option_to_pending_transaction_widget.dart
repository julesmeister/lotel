class OptionToPendingTransactionWidget extends StatefulWidget {
  const OptionToPendingTransactionWidget({
    super.key,
    required this.booking,
    required this.transactions,
  });

  final DocumentReference? booking;
  final List<DocumentReference>? transactions;

  @override
  State<OptionToPendingTransactionWidget> createState() =>
      _OptionToPendingTransactionWidgetState();
}

class _OptionToPendingTransactionWidgetState
    extends State<OptionToPendingTransactionWidget> {
  late OptionToPendingTransactionModel _model;

  @override
  void setState(VoidCallback callback) {
    super.setState(callback);
    _model.onUpdate();
  }

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => OptionToPendingTransactionModel());

    WidgetsBinding.instance.addPostFrameCallback((_) => safeSetState(() {}));
  }

  @override
  void dispose() {
    _model.maybeDispose();

    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.all(16.0),
      child: Container(
        width: 300.0,
        decoration: BoxDecoration(
          color: FlutterFlowTheme.of(context).secondaryBackground,
          boxShadow: const [
            BoxShadow(
              blurRadius: 4.0,
              color: Color(0x33000000),
              offset: Offset(
                0.0,
                2.0,
              ),
            )
          ],
          borderRadius: BorderRadius.circular(12.0),
        ),
        child: Padding(
          padding: const EdgeInsetsDirectional.fromSTEB(0.0, 0.0, 0.0, 12.0),
